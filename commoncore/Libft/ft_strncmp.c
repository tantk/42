#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	char	*s1_ptr;
	char	*s2_ptr;

	s1_ptr = (char *)s1;
	s2_ptr = (char *)s2;
	while (*s1 != *s2 && *s1 && n)
	{
		s1++;
		s2++;
		n--;
	}
	return (*s1 - *s2);
}
